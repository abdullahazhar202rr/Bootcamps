// Regular function declaration
function sayHello(name) {
    return `Hello, ${name}!`;
  }
  
  console.log(sayHello("Abdullah")); // Output: Hello, Abdullah!


  
  // Arrow function with parameters
const sayHi = (name) => {
    console.log(name);
  };

  console.log(sayHi("Ali")); // Output: Ali
  